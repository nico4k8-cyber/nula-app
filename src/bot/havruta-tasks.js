export function getTask(id) {
  return HAVRUTA_TASKS.find((t) => t.id === id) || HAVRUTA_TASKS[0];
}

/**
 * Найти совпадение ключевых слов в тексте пользователя
 * Возвращает первое совпадение или null
 */
export function matchKeyword(text, keywords) {
  const normalized = text
    .toLowerCase()
    .replace(/ё/g, "е")
    .trim();

  for (const [pattern, response] of Object.entries(keywords)) {
    const regex = new RegExp(pattern, "i");
    if (regex.test(normalized)) {
      return response;
    }
  }
  return null;
}
