    const negationPatterns = [
      new RegExp(`${WB}нет(?:[^а-яёА-ЯЁ]|$)`),
      new RegExp(`${WB}не\\s`),
      /нельзя/, /невозможн/, new RegExp(`${WB}упа[дл]`), /опасн/, /плох/, /страшн/, /проблем/, /мешает/, /не получ/, /не выйд/, /не пройд/, new RegExp(`${WB}бед[аы]`), /трудн/, /сложн/, /скольз.*пол/, /не в порядк/
    ];
