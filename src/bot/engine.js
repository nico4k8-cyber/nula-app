export const WANT_HINT = ["давай", "да", "ок", "окей", "покажи", "хочу", "конечно", "подскажи", "подсказку", "намекни", "намекни мне", "не знаю давай", "хочу подсказку"];

export function countHits(text, markers) {
  let count = 0;
  for (const m of markers) {
    if (hasMarker(text, m)) count++;
  }
  return count;
}

// Проверяет, содержит ли текст маркеры, специфичные для конкретной ветки решения
// Требует маркеры эффекта/результата (near_miss), которые описывают ЧТО происходит
export function hasBranchSpecificMarkers(text, branch) {
  if (!branch) return false;
  const n = normalize(text);

  // Проверяем near_miss_markers - маркеры эффекта/результата
  // Это то, что показывает ребёнок понимает ПОЧЕМУ это решение работает
  // НО: исключаем маркеры, которые совпадают с основными маркерами (они не показывают понимание)
  const mainMarkers = branch.markers || [];
  const effectMarkers = (branch.near_miss_markers || []).filter(m =>
    !mainMarkers.some(mm => mm === m) // Exclude if marker name is the same
  );

  for (const marker of effectMarkers) {
    if (hasMarker(n, marker)) {
      return true;
    }
  }

  // Если есть слова объяснения + 4+ слов, это показывает понимание
  const hasExplanationWord = /\b(потому|чтобы|тогда|поэтому|кроме|кроме|давайте)\b/i.test(n);
  if (hasExplanationWord && n.split(" ").length >= 4) {
    return true;
  }

  return false;
}

export function recognize(text, task, found = []) {
  const n = normalize(text);
  const textOnly = n.replace(/[^\p{L}\d]/gu, "");
  if (!textOnly || textOnly.length < 2) return { type: "emoji_spam" };
  if (!/[а-яё]/i.test(n)) return { type: "not_russian" };
  if (GIVE_UP.some((g) => n.includes(g))) return { type: "give_up" };
  if (task.trap && countHits(n, task.trap.markers) > 0) return { type: "trap" };

  let best = null, bestC = 0, isShort = false;
  for (const [id, b] of Object.entries(task.branches)) {
    const markerHits = countHits(n, b.markers);
    const actionHits = b.action_verbs ? countHits(n, b.action_verbs) : 0;
    const phraseHits = b.phrases ? b.phrases.filter(p => n.includes(p) && p.includes(" ")).length : 0;

    // Full solution: Phrase OR (Marker AND Action)
    if (phraseHits > 0 || (markerHits > 0 && actionHits > 0)) {
      let c = markerHits + actionHits + phraseHits * 3;
      if (c > bestC || (c === bestC && isShort)) {
        bestC = c;
        best = id;
        isShort = false;
      }
    }
    // Short match: Marker only
    else if (markerHits > 0 && !bestC) {
      // Only set if we don't have a best yet (prefer first match for ties)
      if (!best) {
        best = id;
        isShort = true;
      }
    }
  }

  if (best) {
    if (found.includes(best)) return { type: "already", id: best };

    const bestBranch = task.branches[best];

    // Проверяем if это exact phrase из массива
    const hasExactPhrase = bestBranch.phrases &&
      bestBranch.phrases.some(p => n.includes(p) && p.includes(" "));

    // Проверяем наличие маркеров эффекта (near_miss) в тексте
    const hasEffectMarkers = bestBranch.near_miss_markers &&
      bestBranch.near_miss_markers.some(m => hasMarker(n, m));

    // Проверяем если marker появляется в phrases (т.е. это известный паттерн для этой ветки)
    const hasMarkerInPhrases = bestBranch.phrases &&
      bestBranch.phrases.some(p => bestBranch.markers.some(m => hasMarker(p, m) && hasMarker(n, m)));

    // Для FULL solution (marker + action):
    // - Exact phrase: всегда detailed
    // - Action + marker + effect markers: detailed
    // - Action + marker где marker из known phrases: detailed (известный паттерн)
    // - Action + marker БЕЗ того и другого: short (просит подробнее)
    let isFinallyDetailed = false;

    if (!isShort) {
      // Full solution (marker + action)
      // Требуем одно из: exact phrase, effect markers, или known pattern
      isFinallyDetailed = hasExactPhrase || hasEffectMarkers || hasMarkerInPhrases;
    } else {
      // Short solution (только marker) - upgrade если exact phrase ИЛИ явное объяснение эффекта
      if (hasExactPhrase) {
        isFinallyDetailed = true;
      } else {
        // Проверяем наличие слов объяснения: "потому что", "чтобы", "и тогда", description effect
        const hasBranchMarkers = hasBranchSpecificMarkers(n, bestBranch);
        isFinallyDetailed = hasBranchMarkers && n.length > 15; // Need explanation + more text
      }
    }

    return {
      type: "found",
      id: best,
      short: isShort && !isFinallyDetailed || (!isShort && !hasExactPhrase && !hasEffectMarkers && !hasMarkerInPhrases),
      detailed: isFinallyDetailed
    };
  }

  for (const [id, b] of Object.entries(task.branches)) {
    if (found.includes(id)) continue;
    const markerHits = countHits(n, b.markers);
    const actionHits = b.action_verbs ? countHits(n, b.action_verbs) : 0;

    // Near miss: count only markers NOT preceded by "не" (negation check)
    const nearMissHits = b.near_miss_markers ? b.near_miss_markers.filter(m => {
      if (!hasMarker(n, m)) return false;
      // Skip if "не" appears right before this marker in the text
      const mShort = m.replace(/ё/g, "е").slice(0, Math.min(m.length, 6));
      const pos = n.indexOf(mShort);
      if (pos > 0) {
        const before = n.slice(Math.max(0, pos - 6), pos);
        if (/не\s*$/.test(before.trimEnd())) return false;
      }
      return true;
    }).length : 0;

    // near_miss fires on: (marker or near_miss_marker), or (marker+action together)
    // action alone does NOT fire near_miss — too many false positives
    if (markerHits > 0 || nearMissHits > 0 || (actionHits > 0 && markerHits > 0)) {
      return { type: "near_miss", id };
    }
  }

  return { type: "fallback", long: n.length > 30 };
}

// buildBingoReply keeps the logic for marking found count and using task names
function buildBingoReply(branch, foundCount, total, isEarlyFind = false, resources = [], aiReply = "") {
  let earlyPrefix = isEarlyFind ? `Невероятно, решение найдено с первого взгляда!\n\n` : "";
  let resourceLink = "";
  if (resources.length > 0 && branch.echo_word) {
    const echoLower = branch.echo_word.toLowerCase();
    const matchedResource = resources.find(r => r.toLowerCase().includes(echoLower.slice(0, 3)));
    if (matchedResource) {
      resourceLink = `\n\n🔗 Помнишь, ты нашёл **${matchedResource.toUpperCase()}** как ресурс? Именно он помог тебе решить задачу по принципу «${branch.principle_child}»! 🌟`;
    }
  }

  const finalReply = aiReply || PICK(branch.reactions_found);

  return `БИНГО! 🎯\n${earlyPrefix}✨ Принцип: «${branch.principle_child}»!\n\n${finalReply}${resourceLink}\n\n📚 Этот приём называется «${branch.name}».\n\nНайдено ${foundCount} из ${total}.`;
}

// Map Pinecone response to recognize-compatible result
function mapPineconeToResult(matches, found) {
  if (!matches || matches.length === 0) return null;

  // Filter out already found branches (except special types)
  const available = matches.filter(m =>
    !found.includes(m.branchId) || m.matchType === 'trap' || m.matchType === 'give_up' || m.matchType === 'problem'
  );
  if (available.length === 0) return null;

  const best = available[0];
  const { branchId, matchType, score } = best;

  // trap type (forbidden answer, e.g. "забор" in monkeys)
  if (matchType === 'trap' && score >= 0.70) {
    return { type: 'trap' };
  }

  // give_up type
  if (matchType === 'give_up' && score >= 0.60) {
    return { type: 'give_up' };
  }

  // problem type (user just describes the problem, not a solution)
  if (matchType === 'problem' && score >= 0.55) {
    return { type: 'fallback', long: false };
  }

  // Smart branch detection: look at top matches for the SAME branch.
  // If near_miss is #1 but full/short is close behind for same branch, prefer the more specific type.
  const sameBranch = available.filter(m => m.branchId === branchId && m.score >= score - 0.05);
  const bestFull = sameBranch.find(m => m.matchType === 'full');
  const bestShort = sameBranch.find(m => m.matchType === 'short');

  // Use the most specific matchType available for this branch
  const effectiveType = bestFull ? 'full' : (bestShort ? 'short' : matchType);
  const effectiveScore = bestFull ? bestFull.score : (bestShort ? bestShort.score : score);

  // full match → BINGO
  if (effectiveType === 'full' && effectiveScore >= 0.72) {
    return { type: 'found', id: branchId, short: false, detailed: true };
  }

  // full match but lower score → ask for details
  if (effectiveType === 'full' && effectiveScore >= 0.55) {
    return { type: 'found', id: branchId, short: true, detailed: false };
  }

  // short match
  if (effectiveType === 'short' && effectiveScore >= 0.55) {
    return { type: 'found', id: branchId, short: true, detailed: false };
  }

  // near_miss (only if no full/short was found nearby)
  if (matchType === 'near_miss' && score >= 0.45) {
    return { type: 'near_miss', id: branchId };
  }

  // Any match above 0.45 but below thresholds → near_miss
  if (score >= 0.45 && branchId && !branchId.startsWith('_')) {
    return { type: 'near_miss', id: branchId };
  }

  return null;
}

export async function classifyWithGemini(text, task, state) {
  const res = await fetch('/api/gemini', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ text, task, state })
  });
  if (!res.ok) throw new Error('Gemini unavailable');
  return await res.json();
}

