export const normalize = (s) => {
  let n = s.toLowerCase().replace(/ё/g, "е").trim().replace(/\s+/g, " ");
  const typos = {
    "пло": "пол",
    "масол": "масло",
    "ковер": "ковёр",
    "испоьзовать": "использовать"
  };
  Object.keys(typos).forEach(t => {
    n = n.replace(new RegExp(`\\b${t}\\b`, 'g'), typos[t]);
  });
  return n.slice(0, 500);
};

